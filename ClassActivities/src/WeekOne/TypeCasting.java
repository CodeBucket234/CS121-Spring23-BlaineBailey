package WeekOne;

public class TypeCasting {
    public static void main(String[] args) {
        int x = 18;
        int y = 5;

        int iz = x / y;
        double dz = x / y;
        double d2z = (double)x / y;

        System.out.println(iz);
        System.out.println(dz);
        System.out.println(d2z);
    }
}
